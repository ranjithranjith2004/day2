// Function Declaration

function a() {
  console.log("Hello");
}

a(); 


// Function Expression

const b = function () {
  console.log("hii");
};

b(); 

// Arrow Function

const c = () => {
  console.log("hi sla");
};

c(); 


// Parameters

function add(a, b) {   
  return a + b;
}

add(33, 3);

// Arguments

function cc(name) {  
  console.log("Hello " + name);
}

cc("Ravi"); 

